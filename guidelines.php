Please use SEARCH first!
Unless you have a strange or unique problem, there is a good chance that it has already been addressed on our forum. Please use the forum's search feature first to see if there are already some good threads on the subject. It is simple to search; simply click the "Search" button at the top right of the page.
Remember when people help you, they are doing YOU a favour!
Be patient, help people out by posting good descriptions of what you need help with, and do not snap at people with nonsense like "if you're not going to help, don't waste my time replying."
